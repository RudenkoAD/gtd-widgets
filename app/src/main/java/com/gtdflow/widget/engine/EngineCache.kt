package com.gtdflow.widget.engine

/**
 * Потокобезопасный кэш ОДНОГО экземпляра с инвалидацией по ошибке.
 *
 * Держит не более одного живого значения на процесс: [acquire] лениво создаёт его
 * через [factory] и дальше возвращает тот же экземпляр. [invalidate] выбрасывает
 * значение (с вызовом [onEvict] для освобождения ресурсов), и СЛЕДУЮЩИЙ acquire
 * создаст свежее.
 *
 * Инвалидация адресная: передаётся именно тот экземпляр, с которым словили ошибку.
 * Если кэш уже держит другой (параллельный вызов успел инвалидировать и пересоздать),
 * ничего не выбрасываем — иначе гонка двух упавших вызовов убила бы уже здоровый
 * свежий экземпляр. [onEvict] зовётся не более одного раза на экземпляр и его сбой
 * глотается: освобождение ресурсов — best-effort и не должно маскировать исходную
 * ошибку вызывающего кода.
 *
 * Класс намеренно без Android-зависимостей — проверяется обычными JVM-тестами.
 */
class EngineCache<T : Any>(
    private val factory: () -> T,
    private val onEvict: (T) -> Unit,
) {
    private val lock = Any()
    private var current: T? = null

    /** Вернуть закэшированный экземпляр или создать новый (под замком — без дублей). */
    fun acquire(): T = synchronized(lock) {
        current ?: factory().also { current = it }
    }

    /**
     * Выбросить [instance] из кэша, если он всё ещё текущий. Возвращает true, если
     * эвикция состоялась (для диагностики/тестов).
     */
    fun invalidate(instance: T): Boolean {
        synchronized(lock) {
            if (current !== instance) return false
            current = null
        }
        try {
            onEvict(instance)
        } catch (_: Throwable) {
            // best-effort: сбой освобождения не должен затенять причину инвалидации
        }
        return true
    }
}
