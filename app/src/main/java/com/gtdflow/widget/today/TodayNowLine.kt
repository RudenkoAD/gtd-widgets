package com.gtdflow.widget.today

import com.gtdflow.widget.engine.TodayItem

/**
 * Позиция строки-маркера «сейчас» в ленте дня (чистая логика, тестируется без Glance).
 *
 * Маркер — отдельная строка-разделитель по РЕАЛЬНОМУ времени показа, не завязанная на
 * наличие элемента в текущем часе (прежняя подсветка «текущего часа» пропадала в
 * разреженные дни и брала час из момента пересчёта, а не показа). Элементы ядром уже
 * отсортированы: сначала «весь день», затем таймингом по возрастанию начала.
 */
object TodayNowLine {

    /**
     * Индекс вставки маркера среди [items] = число элементов «выше сейчас»: все «весь
     * день»/без времени (они всегда сверху) плюс таймингом, начавшиеся строго раньше
     * [nowMinutes]. Диапазон 0..items.size (0 — над всеми, size — под всеми).
     */
    fun position(items: List<TodayItem>, nowMinutes: Int): Int =
        items.count { item ->
            val start = item.startMinutes
            item.allDay || start == null || start < nowMinutes
        }
}
